package day03_comments_scape_sequence;

public class Quotes { public static void main (String[]args){
    System.out.println("Test");
    System.out.println("Test");
    System.out.println("Test");
    System.out.println("Test");    System.out.println("Test");
    System.out.println("Test");
    System.out.println("Test");
    System.out.println("Test");
    System.out.println("I like Java programming");
    System.out.println("I like \"Java\" programming");//This is a double quote
    System.out.println("ON\\OFF");
    System.out.println("The car has on \\\\ off button");


}
}
